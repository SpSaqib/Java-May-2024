package decisionMakingStatement;

public class Switch1 {

	public static void main(String[] args) {
		int a=3;
		
		switch(a)
		{
			case 1:
			{
				System.out.println("I am from case 1");
			}
			
			case 2:
			{
				System.out.println("I am from case 2");
			}
			
			case 3:
			{
				System.out.println("I am from case 3");
			}
			case 4:
			{
				System.out.println("I am from case 4");
				
			}
			case 5:
			{
				System.out.println("I am from case 5");
			}
		
		}

	}

}
